#Diccionarios
# Son un tipo de estructura de datos que permite guardar un conjunto no ordenado de pares clave-valor,
# siendo las claves unicas dentro de un mismo diccionario (es decir que no pueden existir dos elementos con una misma clave)

ejemplo = { "clave" : "valor" }

{ "orden" : { "clave1" : "valor1"} } 

ejemplo ["clave"]
ejemplo.get("clave")

ejemplo ["orden1"] = "valor_orden"